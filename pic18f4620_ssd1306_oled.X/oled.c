static void OLED_Cmd(uint8_t cmd)
{
    uint8_t buf[2] = {0x00, cmd};
    I2C_SW_Write(OLED_ADDR, buf, 2);
}

static uint8_t OLED_Data(const uint8_t *data, uint8_t len)
{
    uint8_t i;
    I2C_SW_Start();
    if (I2C_SW_WriteByte(OLED_ADDR << 1)) {
        I2C_SW_Stop();
        return 0;
    }
    if (I2C_SW_WriteByte(0x40)) {
        I2C_SW_Stop();
        return 0;
    }
    for (i = 0; i < len; i++) {
        if (I2C_SW_WriteByte(data[i])) {
            I2C_SW_Stop();
            return 0;
        }
    }
    I2C_SW_Stop();
    return 1;
}

void OLED_Init(void)
{
    __delay_ms(100);
    OLED_Cmd(0xAE);     // Display OFF
    OLED_Cmd(0xD5);     // Oscillator frequency
    OLED_Cmd(0x80);
    OLED_Cmd(0xA8);     // Multiplex ratio
    OLED_Cmd(0x1F);     // 32 líneas
    OLED_Cmd(0xD3);     // Display offset
    OLED_Cmd(0x00);
    OLED_Cmd(0x40);     // Start line 0
    OLED_Cmd(0x8D);     // Charge pump
    OLED_Cmd(0x14);     // Enable
    OLED_Cmd(0x20);     // Memory mode
    OLED_Cmd(0x00);     // Horizontal
    OLED_Cmd(0xA1);     // Segment remap
    OLED_Cmd(0xC8);     // COM scan direction
    OLED_Cmd(0xDA);     // COM pins
    OLED_Cmd(0x02);     // Para 128x32
    OLED_Cmd(0x81);     // Contrast
    OLED_Cmd(0xCF);
    OLED_Cmd(0xD9);     // Pre-charge
    OLED_Cmd(0xF1);
    OLED_Cmd(0xDB);     // VCOM detect
    OLED_Cmd(0x40);
    OLED_Cmd(0xA4);     // Output from GRAM
    OLED_Cmd(0xA6);     // Normal display
    OLED_Cmd(0x2E);     // Deactivate scroll
    OLED_Cmd(0xAF);     // Display ON
    OLED_Clear();
    OLED_Update();
}

void OLED_Clear(void)
{
    for (uint8_t p = 0; p < OLED_PAGES; p++)
        for (uint8_t c = 0; c < OLED_WIDTH; c++)
            oled_buf[p][c] = 0x00;
}

void OLED_Update(void)
{
    for (uint8_t p = 0; p < OLED_PAGES; p++) {
        OLED_Cmd(0xB0 | p);
        OLED_Cmd(0x00);
        OLED_Cmd(0x10);
        OLED_Data(oled_buf[p], OLED_WIDTH);
    }
}

void OLED_FillScreen(uint8_t pattern)
{
    for (uint8_t p = 0; p < OLED_PAGES; p++)
        for (uint8_t c = 0; c < OLED_WIDTH; c++)
            oled_buf[p][c] = pattern;
    OLED_Update();
}

static void OLED_PutChar(uint8_t row, uint8_t col, char c)
{
    uint8_t idx;
    if (c < 32 || c > 126) c = '?';
    idx = c - 32;
    for (uint8_t i = 0; i < 5; i++) {
        oled_buf[row][col + i] = font5x7[idx][i];
    }
}

void OLED_PutStr(uint8_t row, uint8_t col, const char *str)
{
    if (row >= OLED_PAGES) return;
    uint8_t x = col * 6;
    while (*str && x < OLED_WIDTH) {
        OLED_PutChar(row, x, *str);
        x += 6;
        str++;
    }
}