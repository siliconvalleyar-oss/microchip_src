// ─────────────────────────────────────────────
//  Primitivas I2C
// ─────────────────────────────────────────────
void Ssd1306::send_cmd(uint8_t cmd) {
    uint8_t buf[2] = { 0x00, cmd };   // Co=0, D/C=0 (command)
    HAL::I2c::write(HAL::I2c::SSD1306_ADDR, buf, 2);
}

void Ssd1306::send_data(const uint8_t* data, uint8_t len) {
    // Máx 32 bytes por llamada I2C (límite del buffer de la función I2C)
    uint8_t buf[33];
    buf[0] = 0x40;  // Co=0, D/C=1 (data)
    if (len > 32) len = 32;
    memcpy(&buf[1], data, len);
    HAL::I2c::write(HAL::I2c::SSD1306_ADDR, buf, len + 1);
}

// ─────────────────────────────────────────────
//  init
// ─────────────────────────────────────────────
bool Ssd1306::init() {
    HAL::I2c::init();

    // Secuencia de inicialización SSD1306 para 128×32
    const uint8_t init_cmds[] = {
        0xAE,       // Display OFF
        0xD5,0x80,  // Clock divide / oscillator
        0xA8,0x1F,  // MUX ratio = 31 (32 filas)
        0xD3,0x00,  // Display offset = 0
        0x40,       // Start line = 0
        0x8D,0x14,  // Charge pump ON
        0x20,0x00,  // Memory addressing: horizontal
        0xA1,       // Segment remap (flip horizontal)
        0xC8,       // COM output scan direction (flip vertical)
        0xDA,0x02,  // COM pins config (128×32: alt=0, remap=0)
        0x81,0x8F,  // Contrast
        0xD9,0xF1,  // Pre-charge period
        0xDB,0x40,  // VCOMH deselect level
        0xA4,       // Display from RAM
        0xA6,       // Normal display (no invert)
        0xAF        // Display ON
    };

    for (uint8_t i = 0; i < sizeof(init_cmds); i++)
        send_cmd(init_cmds[i]);

    clear();
    display();
    return true;
}

// ─────────────────────────────────────────────
//  Framebuffer
// ─────────────────────────────────────────────
void Ssd1306::clear() {
    memset(_buf, 0, sizeof(_buf));
}

void Ssd1306::display() {
    // Establecer rango completo: col 0..127, page 0..3
    send_cmd(0x21); send_cmd(0x00); send_cmd(0x7F);  // col range
    send_cmd(0x22); send_cmd(0x00); send_cmd(0x03);  // page range

    // Enviar framebuffer en bloques de 32 bytes
    for (uint16_t i = 0; i < sizeof(_buf); i += 32) {
        send_data(&_buf[i], 32);
    }
}

void Ssd1306::set_contrast(uint8_t c) {
    send_cmd(0x81); send_cmd(c);
}

// ─────────────────────────────────────────────
//  Texto
//  Fuente 5×7 → escalada a 6 columnas (5px + 1 espacio)
//  Pantalla 128×32 = 21 chars × 4 filas (8px por fila = 1 page)
// ─────────────────────────────────────────────
void Ssd1306::set_cursor(uint8_t col, uint8_t row) {
    _cursor_col = col < 21 ? col : 20;
    _cursor_row = row < 4  ? row : 3;
}

void Ssd1306::put_char(char c) {
    if (_cursor_col >= 21 || _cursor_row >= 4) return;
    if (c < 32 || c > 127) c = '?';
    const uint8_t* glyph = FONT5x7[c - 32];

    uint16_t base = (uint16_t)(_cursor_row * 128 + _cursor_col * 6);
    for (uint8_t i = 0; i < 5; i++)
        _buf[base + i] = glyph[i];
    _buf[base + 5] = 0x00;  // Espacio
    _cursor_col++;
}

void Ssd1306::put_string(const char* s, uint8_t max_chars) {
    uint8_t n = 0;
    while (*s && n < max_chars) { put_char(*s++); n++; }
}

void Ssd1306::put_line(uint8_t row, const char* s) {
    if (row >= 4) return;
    // Borrar línea en framebuffer
    memset(&_buf[row * 128], 0, 128);
    set_cursor(0, row);
    put_string(s, 21);
}

// ─────────────────────────────────────────────
//  Vistas de aplicación
// ─────────────────────────────────────────────
void Ssd1306::show_node_mode(NodeRole role) {
    switch (role) {
        case NodeRole::COORDINATOR: put_line(0, "COORDINATOR     "); break;
        case NodeRole::ROUTER:      put_line(0, "ROUTER          "); break;
        case NodeRole::END_DEVICE:  put_line(0, "END DEVICE      "); break;
    }
}

void Ssd1306::show_net_status(const char* status) {
    put_line(1, status);
}

void Ssd1306::show_last_msg(const char* msg) {
    put_line(2, msg);
}

void Ssd1306::show_crypto_status(bool ok, int8_t rssi_dbm) {
    char buf[22];
    // "AES-OK RSSI=-75dBm"  ó  "HMAC-ERR!"
    if (ok) {
        int8_t r = rssi_dbm;
        // Mini itoa para evitar sprintf
        buf[0]='A';buf[1]='E';buf[2]='S';buf[3]='-';buf[4]='O';buf[5]='K';
        buf[6]=' ';buf[7]='R';buf[8]='S';buf[9]='S';buf[10]='I';buf[11]='=';
        int pos = 12;
        if (r < 0) { buf[pos++] = '-'; r = (int8_t)(-r); }
        if (r >= 100) { buf[pos++] = '0' + r/100; r %= 100; }
        buf[pos++] = '0' + r/10;
        buf[pos++] = '0' + r%10;
        buf[pos++] = 'd'; buf[pos++] = 'B'; buf[pos++] = 'm';
        buf[pos] = '\0';
    } else {
        const char* e = "HMAC-ERR!       ";
        for (int i = 0; i < 21; i++) buf[i] = e[i];
        buf[21] = '\0';
    }
    put_line(3, buf);
}

void Ssd1306::show_all(NodeRole role, const char* net_status,
                       const char* last_msg, bool crypto_ok, int8_t rssi) {
    show_node_mode(role);
    show_net_status(net_status);
    show_last_msg(last_msg);
    show_crypto_status(crypto_ok, rssi);
    display();
}

